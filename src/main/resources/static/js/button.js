// Create a function to set the style
window.setButtonStyle = function (button) {
    button.style.backgroundColor = '#8be88d'; // Set the background color
    button.style.border = 'none'; // Remove the border
    button.style.color = 'white'; // Set the text color
    button.style.padding = '10px 20px'; // Set the padding, reduce the width
    button.style.textAlign = 'center'; // Set the text to center
    button.style.textDecoration = 'none'; // Remove the text decoration
    button.style.display = 'inline-block'; // Set to inline-block element
    button.style.fontSize = '16px'; // Set the font size
    button.style.margin = '4px 2px'; // Set the margin
    button.style.cursor = 'pointer'; // Set the cursor style to hand
    button.style.borderRadius = '12px'; // Set the border radius
}
